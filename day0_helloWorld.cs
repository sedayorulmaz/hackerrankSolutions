using System;
using System.Collections.Generic;
using System.IO;


class Solution {
    static void Main(String[] args) {
        // Declare a variable named 'inputString' to hold our input.
        String inputString; 
        
        // Read a full line of input from Console.ReadLine(); and save it to the variable, input_string.
        inputString = Console.ReadLine(); 
        
        // Print "Hello, World." via Console.WriteLine (or Console.Write)
        Console.WriteLine("Hello, World.");
        
        // Output the the String via Console.WriteLine or (or Console.Write)
        Console.WriteLine(inputString);
    }
}
